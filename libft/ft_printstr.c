#include "libft.h"

void	ft_printstr(char *str, int *c)
{
	if (!str)
	{
		*c += write(1, "(null)", 6);
		return ;
	}
	while (*str)
	{
		write(1, str, 1);
		*c += 1;
		str++;
	}
}
