#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		len;
	int		i;
	char	*cpy;

	len = ft_strlen(s);
	cpy = (char *)malloc(sizeof (char) * (len + 1));
	if (cpy == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
